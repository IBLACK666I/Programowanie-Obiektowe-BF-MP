package utils;

/**
 * Klasa reprezentuje punkt
 */
public class Point {

    /**
     * Współrzędna x
     */
    public int x;

    /**
     * Współrzędna y
     */
    public int y;

    /**
     * Konstruktor klasy
     * @param x współrzędna x
     * @param y współrzędna y
     */
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
